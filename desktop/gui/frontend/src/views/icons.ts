export function shortName(path?: string): string {
    return String(path || '').split(/[\\/]/).filter(Boolean).pop() || path || '';
}

export function renderAvatarMarkup(avatarVal?: string, fallbackText = 'D'): string {
    if (avatarVal) {
        return `<img src="${escapeAttr(avatarVal)}" class="avatar-img" alt="Avatar" />`;
    }
    return `<span class="avatar-fallback">${escapeHTML(fallbackText.charAt(0).toUpperCase())}</span>`;
}

export function qrImageURL(pageUrl?: string): string {
    if (!pageUrl) return '';
    return `https://api.qrserver.com/v1/create-qr-code/?size=200x200&data=${encodeURIComponent(pageUrl)}`;
}
